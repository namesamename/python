#탈출 문자

print('백문이불여일견 \n 백견이불여일타') #\n = 줄바꿈

#저는 '이지은'입니다.
print("저는 '이지은'입니다.") #따옴표 혼용 시 문장에 삽입 가능
print('저는 \'이지은\'입니다.') #\", \' = 문장 내에서 따옴표 사용

print('D:\\이지은\\10_Python>') #\\ = 문장 내에서 \ 사용

print('Red Apple \rPine') #\r = 커서를 맨 앞으로 이동

print('Redd\b Apple') #\b = 백스페이스(한 글자 삭제)

print('Red\tApple') #\t = tab